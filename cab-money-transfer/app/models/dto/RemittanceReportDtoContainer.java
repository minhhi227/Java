package models.dto;

import java.io.Serializable;
import java.util.List;

/**
 * @author Vichrak
 * 
 */
public class RemittanceReportDtoContainer implements Serializable{
    
    private static final long serialVersionUID = 1L;
    private List<RemittanceReportDto> _remittanceReportDtos;
    private boolean _showSenderAddress;
    private boolean _showSenderDOB;
    private boolean _showSenderIdentityNo;
    private boolean _showExpiredDate;
    
    public RemittanceReportDtoContainer(){
        super();
    }
    
    public RemittanceReportDtoContainer(final List<RemittanceReportDto> remittanceReportDtos, final boolean showSenderAddress, final boolean showSenderDOB, final boolean showSenderIdentityNo, final boolean showExpiredDate){
        super();
        _remittanceReportDtos = remittanceReportDtos;
        _showSenderAddress = showSenderAddress;
        _showSenderDOB = showSenderDOB;
        _showSenderIdentityNo = showSenderIdentityNo;
        _showExpiredDate = showExpiredDate;
    }
    
    /**
     * @return the remittanceReportDtos
     */
    public List<RemittanceReportDto> getRemittanceReportDtos(){
        return _remittanceReportDtos;
    }
    
    /**
     * @param remittanceReportDtos 
     *          the remittanceReportDtos to set
     */
    public void setRemittanceReportDtos(final List<RemittanceReportDto> remittanceReportDtos){
        _remittanceReportDtos = remittanceReportDtos;
    }
    
    /**
     * @return the showSenderAddress
     */
    public boolean isShowSenderAddress(){
        return _showSenderAddress;
    }
    
    /**
     * @param showSenderAddress 
     *          the showSenderAddress to set
     */
    public void setShowSenderAddress(final boolean showSenderAddress){
        _showSenderAddress = showSenderAddress;
    }
    
    /**
     * @return the showSenderDOB
     */
    public boolean isShowSenderDOB(){
        return _showSenderDOB;
    }
    
    /**
     * @param showSenderDOB 
     *          the showSenderDOB to set
     */
    public void setShowSenderDOB(final boolean showSenderDOB){
        _showSenderDOB = showSenderDOB;
    }
    
    /**
     * @return the showSenderIdentityNo
     */
    public boolean isShowSenderIdentityNo(){
        return _showSenderIdentityNo;
    }
    
    /**
     * @param showSenderIdentityNo 
     *          the showSenderIdentityNo to set
     */
    public void setShowSenderIdentityNo(final boolean showSenderIdentityNo){
        _showSenderIdentityNo = showSenderIdentityNo;
    }
    
    /**
     * @return the showExpiredDate
     */
    public boolean isShowExpiredDate(){
        return _showExpiredDate;
    }
    
    /**
     * @param showExpiredDate 
     *          the showExpiredDate to set
     */
    public void setShowExpiredDate(final boolean showExpiredDate){
        _showExpiredDate = showExpiredDate;
    }
    
}
